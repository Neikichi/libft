int	ft_sqrt(int nb)
{
	int	g;
	int	lg;

	g = nb / 2;
	lg = 0;
	if (nb < 0)
		return (0);
	if (nb == 0 || nb == 1)
		return (nb);
	while (lg != g)
	{
		lg = g;
		g = (g + nb / g) / 2;
	}
	if (g * g == nb)
		return (g);
	else
		return (0);
}
