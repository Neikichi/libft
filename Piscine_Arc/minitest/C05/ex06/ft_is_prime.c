int	ft_is_prime(int nb)
{
	int	idx;

	idx = 2;
	if (nb <= 1)
		return (0);
	while (idx <= nb / idx)
	{
		if (nb % idx == 0)
			return (0);
		idx++;
	}
	return (1);
}
