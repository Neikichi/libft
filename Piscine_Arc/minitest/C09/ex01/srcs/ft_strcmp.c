#include "ft.h"

int	ft_strcmp(char *s1, char *s2)
{
	int	cnt;

	cnt = 0;
	while (*s1 || *s2)
	{
		cnt = cnt + (*s1 - *s2);
		if (cnt != 0)
			return (cnt);
		s1++;
		s2++;
	}
	return (0);
}
